#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i] != '\0')
		i++;
	while ((char)s2[j] != '\0')
	{
		s1[i + j] = (char)s2[j];
		j++;
	}
	s1[i + j] = '\0';
	return ((char *)s1);
}
