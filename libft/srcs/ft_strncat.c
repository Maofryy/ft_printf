#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	int		j;
	size_t	k;

	i = 0;
	j = 0;
	k = 0;
	while (s1[i] != '\0')
		i++;
	while ((k < n) && (char)s2[j] != '\0')
	{
		s1[i + j] = (char)s2[j];
		j++;
		k++;
	}
	s1[i + j] = '\0';
	return ((char *)s1);
}
